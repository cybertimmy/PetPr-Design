import UIKit

extension UIColor {
    static func brownColor() -> UIColor {
        return(UIColor(red: 236/255, green: 177/255, blue: 118/255, alpha: 1.0))
    }
    static func darkBGColor() -> UIColor {
        UIColor(red: 32/255, green: 27/255, blue: 28/255, alpha: 100)
    }
    static func subtitleColor() -> UIColor {
        UIColor(red: 97/255, green: 95/255, blue: 93/255, alpha: 100)
    }
}
